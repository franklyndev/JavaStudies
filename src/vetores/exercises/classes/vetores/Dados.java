package vetores.exercises.classes.vetores;

public class Dados {
    private String name;
    private int age;
    private double height;


    public Dados(String name, int age, double height, int n) {
        this.name = name;
        this.age = age;
        this.height = height;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public double getHeight() {
        return height;
    }
}


